                <h1>Shakshuka</h1>
                <p>A Shakshuka é um prato tradicional do Oriente Médio que combina ovos cozidos em um molho rico e saboroso de tomate, pimentão e especiarias.</p>

                <h2>Ingredientes</h2>
                <ul>
                    <li>4 ovos</li>
                    <li>1 cebola grande, picada</li>
                    <li>2 dentes de alho, picados</li>
                    <li>1 pimentão vermelho, cortado em cubos</li>
                    <li>1 pimentão amarelo, cortado em cubos</li>
                    <li>6 tomates maduros, picados</li>
                    <li>2 colheres de sopa de azeite de oliva</li>
                    <li>1 colher de chá de cominho em pó</li>
                    <li>1 colher de chá de páprica</li>
                    <li>1/2 colher de chá de pimenta vermelha em flocos (opcional, para um toque picante)</li>
                    <li>Sal e pimenta-do-reino a gosto</li>
                    <li>Folhas de coentro fresco para decorar (opcional)</li>
                </ul>

                <h2>Instruções</h2>
                <p>Aqueça o azeite em uma frigideira grande em fogo médio-alto.<br>
                Adicione a cebola e refogue por 2-3 minutos até ficar macia e translúcida.<br>
                Adicione o alho, os pimentões e cozinhe por mais 5 minutos, mexendo ocasionalmente, até que os vegetais estejam macios.<br>
                Adicione os tomates picados, o cominho, a páprica, a pimenta vermelha em flocos (se estiver utilizando), sal e pimenta-do-reino. Cozinhe por cerca de 10-15 minutos até que os tomates comecem a desmanchar e o molho engrosse um pouco.<br>
                Com as costas de uma colher, faça pequenos espaços no molho e quebre um ovo em cada espaço. Tempere os ovos com uma pitada de sal.<br>
                Reduza o fogo para médio-baixo, tampe a frigideira e cozinhe por mais 5-7 minutos, ou até que as claras estejam firmes, mas as gemas ainda estejam líquidas (ou a seu gosto de cocção).<br>
                Retire do fogo e deixe descansar por alguns minutos.<br>
                Decore com folhas de coentro fresco, se desejar, e sirva quente com pão fresco crustoso para mergulhar no molho delicioso de Shakshuka.</p>

                <p>Aproveite sua Shakshuka caseira e cheia de sabor!</p>
