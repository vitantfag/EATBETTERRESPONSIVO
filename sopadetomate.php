                <h1>Sopa de Tomate</h1>
                <p>A sopa de tomate é um prato delicioso, nutritivo e versátil que agrada paladares de todas as idades. É perfeita para dias frios, como um acompanhamento leve ou até mesmo como uma refeição completa.</p>

                <h2>Ingredientes</h2>
                <ul>
                    <li>1,5 kg de tomates maduros</li>
                    <li>1 cebola grande, picada</li>
                    <li>2 dentes de alho, picados</li>
                    <li>2 colheres de sopa de azeite de oliva</li>
                    <li>4 xícaras de caldo de legumes ou água</li>
                    <li>1 colher de chá de açúcar (opcional, para equilibrar a acidez dos tomates)</li>
                    <li>Sal e pimenta a gosto</li>
                    <li>Folhas de manjericão fresco para decorar (opcional)</li>
                </ul>

                <h2>Instruções</h2>
                <p>Faça um corte em forma de "X" na base de cada tomate e mergulhe-os em água fervente por cerca de 1 minuto. Retire da água e descasque os tomates. Corte em pedaços grandes e reserve.<br>
                Em uma panela grande, aqueça o azeite de oliva em fogo médio. Adicione a cebola e o alho picados e refogue até ficarem macios e levemente dourados.<br>
                Acrescente os tomates picados à panela e cozinhe por cerca de 5 minutos, mexendo ocasionalmente, até que comecem a desmanchar.<br>
                Adicione o caldo de legumes ou água à panela e deixe ferver. Reduza o fogo e deixe a sopa cozinhar por mais 15-20 minutos, para que os sabores se misturem bem.<br>
                Use um liquidificador ou mixer de mão para triturar a sopa até obter uma consistência homogênea. Se preferir uma sopa mais cremosa, passe por uma peneira ou coador fino.<br>
                Tempere com sal, pimenta e açúcar, se estiver utilizando, ajustando ao seu gosto pessoal.<br>
                Sirva a sopa de tomate quente, decorada com folhas de manjericão fresco, se desejar. Acompanhe com pão fresco ou croutons para uma refeição reconfortante e deliciosa.</p>

                <p>Aproveite sua sopa de tomate caseira e cheia de sabor!</p>
                
