<h1>Ratatouille</h1>
<p>O Ratatouille é um prato francês clássico que combina a beleza vibrante dos legumes mediterrâneos com o sabor rico e aconchegante de um refogado caseiro.</p>

<h2>Ingredientes</h2>
<ul>
    <li>1 berinjela média</li>
    <li>1 abobrinha média</li>
    <li>1 pimentão vermelho</li>
    <li>1 pimentão amarelo</li>
    <li>2 tomates grandes</li>
    <li>1 cebola grande</li>
    <li>2 dentes de alho</li>
    <li>4 colheres de sopa de azeite de oliva</li>
    <li>1 colher de chá de tomilho fresco (ou 1/2 colher de chá de tomilho seco)</li>
    <li>1 colher de chá de alecrim fresco picado (opcional)</li>
    <li>Sal e pimenta-do-reino a gosto</li>
    <li>Folhas de manjericão fresco para decorar (opcional)</li>
</ul>

<h2>Instruções</h2>
<p>Preaqueça o forno a 200°C.<br>
Corte todos os legumes em rodelas finas, de aproximadamente 0,5 cm de espessura, mantendo o formato original de cada um.<br>
Em uma assadeira grande, disponha as rodelas de legumes em camadas alternadas, sobrepondo levemente uma sobre a outra. Comece com as fatias de tomate e continue com berinjela, abobrinha, pimentões e cebola, repetindo até terminar os legumes.<br>
Em um pequeno recipiente, misture o alho picado, o azeite de oliva, o tomilho, o alecrim (se estiver utilizando), sal e pimenta-do-reino.<br>
Regue os legumes com a mistura de temperos, garantindo que todos fiquem bem cobertos.<br>
Cubra a assadeira com papel alumínio e leve ao forno por aproximadamente 45-55 minutos, ou até que os legumes estejam macios, mas ainda firmes.<br>
Retire o papel alumínio nos últimos 10 minutos de cozimento para dourar levemente os legumes.<br>
Retire do forno e deixe descansar por alguns minutos antes de servir. Decore com folhas de manjericão fresco, se desejar.<br>
Sirva o Ratatouille quente como prato principal acompanhado de arroz, quinoa ou pão fresco. Também pode ser servido como acompanhamento para carnes grelhadas.</p>

<p>Aproveite seu Ratatouille fresco e cheio de cores!</p>   

